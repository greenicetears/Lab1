package mypackage;

public class Occupation
{
    private String occupation;

    public Occupation(String o){
        this.occupation = o;
    }

    public void setOccupation(String o){
        this.occupation = o;
    }

    public String getOccupation(){
        return this.occupation;
    }

    public String toString(){
        return this.occupation;
    }
}

